    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    if (isIDvalid(jTextField4.getText()) && !jTextField1.getText().isEmpty() && !jTextField5.getText().isEmpty()&& !jTextField6.getText().isEmpty()){       
                
        try {
            String username1 = jTextField5.getText();
            String password1 = jTextField6.getText();
            String name1 = jTextField1.getText();
            String ID1 = jTextField4.getText();
            User u = new User(username1,password1,name1,ID1);
            this.users.add(u);
            this.userFile.write(u.getUsername());
            this.passwordFile.write(u.getPassword());
            this.IDFile.write(u.getID());
            this.nameFile.write(u.getName());
        } catch (IOException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.jTextField1.setText("");
        this.jTextField4.setText("");
        this.jTextField5.setText("");
        this.jTextField6.setText("");
        this.jDialog1.setVisible(false);
        this.setVisible(true);
        this.pack();
        
    }
   else
         JOptionPane.showMessageDialog(this, "error2");
    
    
    }//GEN-LAST:event_jButton5ActionPerformed

