    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      String UName=sgfield.getText();
      String Pw=pwfield.getText();
      Boolean loggedIn=false;
      for(User current: users)
          
      {
          if (UName.equals(current.getUsername()))
          {
              if(Pw.equals(current.getPassword())){
                  sgfield.setText("");
                  pwfield.setText("");
                  loggedIn=true;
                  this.setVisible(false);
                  this.jDialog2.setVisible(true);
                  this.jDialog2.pack();
                  this.JLable20.setText("שלום, "+current.getName());
              }
          }
          
      }
      if (!loggedIn)
          JOptionPane.showMessageDialog(this, " שם משתמש או ססמה אינם נכונים");
    }//GEN-LAST:event_jButton3ActionPerformed
    
